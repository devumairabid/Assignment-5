var a : any= prompt("Enter percentage to check grade");
if(a>=90){
    document.write("Grade A")
}else if(a>=80){
    document.write("Grade B")
}else if (a>=70){
    document.write("Grade C")
}else if (a>=60){
    document.write("Grade D")
}else if (a>=40){
    document.write("Grade E")
}else {
    document.write("Grade F")
}